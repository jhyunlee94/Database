1) 송강 교수가 강의하는 과목을 검색하세요.
SELECT A.PNAME, B.CNAME
    FROM PROFESSOR A, COURSE B
    WHERE A.PNO = B.PNO
    AND A.PNAME = '송강';

SELECT PNAME, CNAME
    FROM PROFESSOR
    NATURAL JOIN COURSE
    WHERE PNAME = '송강';
    
SELECT PNAME, CNAME
    FROM PROFESSOR
    JOIN COURSE USING(PNO)
    WHERE PNAME = '송강';
