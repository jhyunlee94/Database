3) 화학과 1학년 학생의 기말고사 성적을 검색하세요.
SELECT C.SNAME, D.RESULT, C.MAJOR
    FROM PROFESSOR A, COURSE B, STUDENT C, SCORE D
    WHERE A.PNO = B.PNO
    AND C.SNO = D.SNO
    AND B.CNO = D.CNO
    AND MAJOR = '화학'
    AND SYEAR = 1;

SELECT SNAME, RESULT, MAJOR
    FROM PROFESSOR
    JOIN COURSE USING (PNO)
    JOIN SCORE USING (CNO)
